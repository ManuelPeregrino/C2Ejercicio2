    private void AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarActionPerformed

            switch (i) {
             case 1:
                EmpleadoRol = new Directivo(Nombre.getText());
                Salida.setText(Empleado.leerdatos("Directivo"));
                break;
            case 2:
                EmpleadoRol = new Operario(Nombre.getText());
                Salida.setText(Empleado.leerdatos("Operario"));
                break;
            case 3:
                EmpleadoRol = new Oficial(Nombre.getText());
                Salida.setText(Empleado.leerdatos("Oficial"));
                break;
            case 4:
                EmpleadoRol = new Tecnico(Nombre.getText());
                Salida.setText(Empleado.leerdatos("Técnico"));
                Nombre.setEditable(false);
                Agregar.setEnabled(false);
                break;
            default:
                Agregar.setEnabled(false);
                Salida.setEditable(false);
                break;
        }
        
        Texto += EmpleadoRol + "\n";
        Salida.setText(Texto);
        Nombre.setText("");
        i++;    
        
        
    }//GEN-LAST:event_AgregarActionPerformed

